<p>Le terme aïkido (aikido en japonais) est composé de trois <a href="https://fr.wikipedia.org/wiki/Kanji">kanjis</a> signifiant :</p>

<p style="padding-left:30%;"> <b>ai</b> : du verbe au, concorder ; harmonie</p>
<p style="padding-left:30%;"> <b>ki</b> : énergie</p>
<p style="padding-left:30%;"> <b>do</b> : la voie</p>
 
<p>L'AÏKIDO est un art martial japonais moderne, art de défense par excellence, contre un ou plusieurs adversaires armés ou non.</p>
<p>Son principe essentiel est d'utiliser l'agressivité de l'attaquant pour le neutraliser par des projections ou des immobilisations et ceci sans nuire à son intégrité physique et mentale.</p>
<p style="padding-left:30%;">Persuader plutôt que dissuader</p>
<p style="padding-left:30%;">Convaincre plutôt que vaincre</p>
<p style="padding-left:30%;">Construire plutôt que détruire</p>
<p>Cet art martial propose à ses pratiquants une démarche de connaissance de soi et des autres, une approche de la gestion des conflits.</p>
<p>Art de vivre par excellence, sa pratique vous apportera équilibre, contrôle de soi et respect des autres. Du fait que l'aïkido n'est pas un art martial compétitif, il peut être pratiqué par tous (hommes et femmes de tout âge ainsi que par les enfants). </p>
